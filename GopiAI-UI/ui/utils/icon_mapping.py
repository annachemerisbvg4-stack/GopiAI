def get_lucide_name(icon_name: str) -> str:
    """
    Преобразование старого имени иконки в имя Lucide.
    
    Args:
        icon_name: Старое имя иконки
        
    Returns:
        str: Имя иконки в формате Lucide
    """
    # Если есть в маппинге, используем его
    if icon_name in ICON_NAME_MAPPING:
        return ICON_NAME_MAPPING[icon_name]
    
    # Если имя уже в формате Lucide (содержит дефисы), возвращаем как есть
    if "-" in icon_name:
        return icon_name
    
    # Иначе возвращаем исходное имя
    return icon_name


